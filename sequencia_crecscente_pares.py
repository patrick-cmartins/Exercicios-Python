# usuário um numero natural e exiba a
# sequencia crescente de zero até o
# numero dado, somente os pares.

n = int(input())
a = int(input())
def menosum ():
    -1
for i in range(n, a, menosum()): #intervalo, até, somando/passo
    print(i, end=' ')
